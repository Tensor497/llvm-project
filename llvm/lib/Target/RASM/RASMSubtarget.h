#ifndef LLVM_LIB_TARGET_RASM_RASMSUBTARGET_H
#define LLVM_LIB_TARGET_RASM_RASMSUBTARGET_H

#include "RASMFrameLowering.h"
#include "RASMISelLowering.h"
#include "RASMInstrInfo.h"
#include "RASMRegisterInfo.h"
#include "MCTargetDesc/RASMBaseInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "RASMGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class RASMSubtarget : public RASMGenSubtargetInfo {
  virtual void anchor();
  bool HasLA64 = false;
  bool HasBasicF = false;
  bool HasBasicD = false;
  bool HasExtLSX = false;
  bool HasExtLASX = false;
  bool HasExtLVZ = false;
  bool HasExtLBT = false;
  bool HasLaGlobalWithPcrel = false;
  bool HasLaGlobalWithAbs = false;
  bool HasLaLocalWithAbs = false;
  unsigned GRLen = 32;
  MVT GRLenVT = MVT::i32;
  RASMABI::ABI TargetABI = RASMABI::ABI_Unknown;
  RASMFrameLowering FrameLowering;
  RASMInstrInfo InstrInfo;
  RASMRegisterInfo RegInfo;
  RASMTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

  /// Initializes using the passed in CPU and feature strings so that we can
  /// use initializer lists for subtarget initialization.
  RASMSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                      StringRef CPU,
                                                      StringRef TuneCPU,
                                                      StringRef FS,
                                                      StringRef ABIName);

public:
  // Initializes the data members to match that of the specified triple.
  RASMSubtarget(const Triple &TT, StringRef CPU, StringRef TuneCPU,
                     StringRef FS, StringRef ABIName, const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const RASMFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const RASMInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const RASMRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }
  const RASMTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  bool is64Bit() const { return HasLA64; }
  bool hasBasicF() const { return HasBasicF; }
  bool hasBasicD() const { return HasBasicD; }
  bool hasExtLSX() const { return HasExtLSX; }
  bool hasExtLASX() const { return HasExtLASX; }
  bool hasExtLVZ() const { return HasExtLVZ; }
  bool hasExtLBT() const { return HasExtLBT; }
  bool hasLaGlobalWithPcrel() const { return HasLaGlobalWithPcrel; }
  bool hasLaGlobalWithAbs() const { return HasLaGlobalWithAbs; }
  bool hasLaLocalWithAbs() const { return HasLaLocalWithAbs; }
  MVT getGRLenVT() const { return GRLenVT; }
  unsigned getGRLen() const { return GRLen; }
  RASMABI::ABI getTargetABI() const { return TargetABI; }
};
} // end namespace llvm

#endif // LLVM_LIB_TARGET_RASM_RASMSUBTARGET_H
